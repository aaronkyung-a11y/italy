export const TOTAL_POINTS = ATTRACTIONS.reduce((sum, a) => sum + a.points.length, 0);

// ID로 빠른 조회
export function findAttraction(id) {
  return ATTRACTIONS.find((a) => a.id === id);
}
export function findPoint(attractionId, pointId) {
  const a = findAttraction(attractionId);
  return a?.points.find((p) => p.id === pointId);
}
